export const getQuestion = (type, usedQuestions = []) => {
    const list = QUESTIONS[type] || QUESTIONS.general;
    const available = list.filter(q => !usedQuestions.includes(q));

    // لو خلصت كل الأسئلة، نرجع نستخدم القائمة كاملة تاني (تصفير التاريخ لهذا النوع)
    const pool = available.length ? available : list;
    return pool[Math.floor(Math.random() * pool.length)];
};

