const DOAA = [
    'اللَّهُمَّ إِنِّي أَسْأَلُكَ الْعَافِيَةَ فِي الدُّنْيَا وَالْآخِرَةِ',
    'رَبَّنَا آتِنَا فِي الدُّنْيَا حَسَنَةً وَفِي الْآخِرَةِ حَسَنَةً وَقِنَا عَذَابَ النَّارِ',
    'اللَّهُمَّ اغْفِرْ لِي وَارْحَمْنِي وَاهْدِنِي وَارْزُقْنِي',
    'رَبِّ اشْرَحْ لِي صَدْرِي وَيَسِّرْ لِي أَمْرِي',
    'اللَّهُمَّ إِنِّي أَعُوذُ بِكَ مِنَ الْهَمِّ وَالْحَزَنِ',
    'اللَّهُمَّ أَعِنِّي عَلَى ذِكْرِكَ وَشُكْرِكَ وَحُسْنِ عِبَادَتِكَ',
];

const RAMADAN = [
    'اللَّهُمَّ إِنَّكَ عَفُوٌّ تُحِبُّ الْعَفْوَ فَاعْفُ عَنِّي',
    'اللَّهُمَّ أَعِنِّي عَلَى صِيَامِهِ وَقِيَامِهِ',
    'اللَّهُمَّ بَلِّغْنَا رَمَضَانَ وَأَعِنَّا عَلَى صِيَامِهِ وَقِيَامِهِ',
];

const handler = async (m, { conn, command }) => {
    const isRamadan = command === 'دعاء-رمضان' || command === 'دعاء_رمضان';
    const list = isRamadan ? RAMADAN : DOAA;
    const dua = list[Math.floor(Math.random() * list.length)];
    const title = isRamadan ? '🌙 *دعاء رمضان*' : '🤲 *دعـاء*';

    await conn.sendMessage(m.chat, {
        text:
            `┌──────────────────┐\n` +
            `│  ${title}  │\n` +
            `└──────────────────┘\n\n` +
            `*${dua}*\n\n` +
            `> _آمين يا رب العالمين_ 🤲`
    }, { quoted: m });
};
handler.command  = ['ادعيه', 'ادعية', 'دعاء-رمضان', 'دعاء_رمضان'];
handler.usage    = ['ادعيه', 'دعاء-رمضان'];
handler.category = 'religion';
export default handler;
