const HADITH = [
    'إنما الأعمال بالنيات، وإنما لكل امرئ ما نوى. [البخاري]',
    'المسلم من سلم المسلمون من لسانه ويده. [البخاري]',
    'لا يؤمن أحدكم حتى يحب لأخيه ما يحب لنفسه. [البخاري]',
    'من كان يؤمن بالله واليوم الآخر فليقل خيراً أو ليصمت. [البخاري]',
    'الدين النصيحة. [مسلم]',
    'خير الناس أنفعهم للناس. [الطبراني]',
    'ابدأ بنفسك. [مسلم]',
    'إن الله جميل يحب الجمال. [مسلم]',
    'اتق الله حيثما كنت. [الترمذي]',
    'من صمت نجا. [الترمذي]',
    'التسبيح نصف الميزان والحمد يملأه. [الترمذي]',
    'الكلمة الطيبة صدقة. [البخاري]',
];

const handler = async (m, { conn }) => {
    const h = HADITH[Math.floor(Math.random() * HADITH.length)];
    await conn.sendMessage(m.chat, {
        text:
            `┌──────────────────┐\n` +
            `│  📜 *حـديـث شـريـف*  │\n` +
            `└──────────────────┘\n\n` +
            `*"${h}"*\n\n` +
            `> _صلى الله على محمد وآله وصحبه_`
    }, { quoted: m });
};
handler.command  = ['حديث'];
handler.usage    = ['حديث'];
handler.category = 'religion';
export default handler;
