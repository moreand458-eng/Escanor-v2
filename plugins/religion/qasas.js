const QASAS = [
    { title: 'قصة سيدنا يوسف', text: 'يوسف عليه السلام كان أجمل الناس وأصدقهم، باعه إخوته، لكن الله كرمه وجعله على خزائن مصر. قال تعالى ﴿نَحْنُ نَقُصُّ عَلَيْكَ أَحْسَنَ الْقَصَصِ﴾' },
    { title: 'قصة سيدنا موسى', text: 'ولد في وقت كان فرعون يقتل الأطفال، فحفظه الله وأعاده لأمه، ثم بعثه لهداية فرعون وبني إسرائيل.' },
    { title: 'قصة أصحاب الكهف', text: 'فتية آمنوا بالله وفروا من ملك ظالم، فحفظهم الله في كهفهم ثلاثمائة سنة وازدادوا تسعاً.' },
    { title: 'قصة سيدنا إبراهيم', text: 'أبو الأنبياء، كسر الأصنام وبنى الكعبة مع ابنه إسماعيل، وامتحنه الله بذبح ابنه فصبر ففداه الله.' },
];
const handler = async (m, { conn }) => {
    const q = QASAS[Math.floor(Math.random() * QASAS.length)];
    await conn.sendMessage(m.chat, {
        text: `📚 *${q.title}*\n\n${q.text}\n\n> _هذه قصص من القرآن الكريم_`
    }, { quoted: m });
};
handler.command  = ['قصص', 'قصه'];
handler.usage    = ['قصص'];
handler.category = 'religion';
export default handler;
