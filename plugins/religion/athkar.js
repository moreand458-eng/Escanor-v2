const ATHKAR = [
    'سبحان الله وبحمده، سبحان الله العظيم',
    'لا إله إلا الله وحده لا شريك له، له الملك وله الحمد وهو على كل شيء قدير',
    'اللهم صل على محمد وعلى آل محمد',
    'الحمد لله على كل حال',
    'لا حول ولا قوة إلا بالله العلي العظيم',
    'أستغفر الله العظيم الذي لا إله إلا هو الحي القيوم وأتوب إليه',
    'سبحان الله وبحمده عدد خلقه ورضا نفسه وزنة عرشه ومداد كلماته',
];

const SABAH = [
    'أَصْبَحْنَا وَأَصْبَحَ الْمُلْكُ للهِ، وَالْحَمْدُ للهِ، لاَ إِلَهَ إِلاَّ اللهُ وَحْدَهُ لاَ شَرِيكَ لَهُ',
    'اللَّهُمَّ بِكَ أَصْبَحْنَا، وَبِكَ أَمْسَيْنَا، وَبِكَ نَحْيَا، وَبِكَ نَمُوتُ وَإِلَيْكَ النُّشُورُ',
    'اللَّهُمَّ أَنْتَ رَبِّي لاَ إِلَهَ إِلاَّ أَنْتَ، خَلَقْتَنِي وَأَنَا عَبْدُكَ',
    'أَعُوذُ بِكَلِمَاتِ اللهِ التَّامَّاتِ مِنْ شَرِّ مَا خَلَقَ',
];

const handler = async (m, { conn, command }) => {
    const isSabah = command === 'اذكار-الصباح' || command === 'اذكار_الصباح';
    const list = isSabah ? SABAH : ATHKAR;
    const dhikr = list[Math.floor(Math.random() * list.length)];
    const title = isSabah ? '🌅 *أذكار الصباح*' : '📿 *ذكـر*';

    await conn.sendMessage(m.chat, {
        text:
            `┌──────────────────┐\n` +
            `│  ${title}  │\n` +
            `└──────────────────┘\n\n` +
            `*${dhikr}*\n\n` +
            `> _اللهم اجعلنا من الذاكرين_`
    }, { quoted: m });
};
handler.command  = ['اذكار', 'اذكار-الصباح', 'اذكار_الصباح'];
handler.usage    = ['اذكار', 'اذكار-الصباح'];
handler.category = 'religion';
export default handler;
