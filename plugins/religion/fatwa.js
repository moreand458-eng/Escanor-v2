const FATAWA = [
    { q: 'ما حكم الصلاة في الوقت المحدد؟', a: 'الصلاة في وقتها فرض واجب، ومن أخرها بلا عذر فقد أثم.' },
    { q: 'ما حكم قراءة القرآن بدون طهارة؟', a: 'يجوز تلاوة القرآن للمحدث حدثاً أصغر، لكن لا يجوز مس المصحف.' },
    { q: 'ما حكم الصيام في السفر؟', a: 'يجوز الفطر في السفر والقضاء بعده، والصيام أفضل لمن لا يشق عليه.' },
    { q: 'ما حكم الدعاء بعد الصلاة؟', a: 'الدعاء بعد الصلاة مستحب، وأوقات الاستجابة كثيرة.' },
    { q: 'هل يجوز سماع الموسيقى؟', a: 'اختلف العلماء في ذلك، والأحوط الابتعاد عن الموسيقى الصاخبة.' },
];
const handler = async (m, { conn }) => {
    const f = FATAWA[Math.floor(Math.random() * FATAWA.length)];
    await conn.sendMessage(m.chat, {
        text: `⚖️ *فتوى*\n\n*السؤال:* ${f.q}\n\n*الجواب:* ${f.a}\n\n> _والله أعلم_`
    }, { quoted: m });
};
handler.command  = ['فتاوي', 'فتوى'];
handler.usage    = ['فتاوي'];
handler.category = 'religion';
export default handler;
